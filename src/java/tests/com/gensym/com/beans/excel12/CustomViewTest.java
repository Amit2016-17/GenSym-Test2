    public void test_Invoke_EightArgs_ShouldRedirectToProxy() throws Exception {
        on(0x60010003).dispatchType(ActiveXRuntimeConstants.AX_INVOKE).args(new Variant(42), new Variant(825), new
                Variant(42L), new Variant(42), new Variant(279), new Variant(935), new Variant(776), new Variant(94))
                .verifyNoReturn(new Void() {
                    @Override
                    public void invoke() throws ActiveXException {
                        component.Invoke(42, new Variant(825), 42L, 42, new Variant(279), new Variant(935), new
                                Variant(776), new Variant(94));
                    }
                });
    }

