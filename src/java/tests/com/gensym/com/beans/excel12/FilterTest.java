    public void test_Invoke_EightArgs_ShouldRedirectToProxy() throws Exception {
        on(0x60010003).dispatchType(ActiveXRuntimeConstants.AX_INVOKE).args(new Variant(42), new Variant(353), new
                Variant(42L), new Variant(42), new Variant(761), new Variant(547), new Variant(963), new Variant(740))
                .verifyNoReturn(new Void() {
                    @Override
                    public void invoke() throws ActiveXException {
                        component.Invoke(42, new Variant(353), 42L, 42, new Variant(761), new Variant(547), new
                                Variant(963), new Variant(740));
                    }
                });
    }

